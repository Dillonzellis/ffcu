<h2 align="center"><strong><u>The Story of Carl Blouin with Family First Credit Union</u></strong></h2>

	<p align="center">By:  Stephen Smith, President and CEO</p>
	<p>I first met Carl Blouin when he worked as our Sales Representative for Enterprise Car Sales (our car buying service).  
I was the Vice President of Lending and worked with Carl weekly so he would help our members make the right choices in buying cars.  I found in dealing with Carl that he gave our members excellent customer service and treated our members like he really cared about their wellbeing.  I had complete trust in him to treat our members like we did.  Carl was a high achiever.  Carl set a record for selling the most cars in a month because of a car sale with us and from helping additional members. We formed a bond and close partnership at that time in 2004 and 2005.  On many occasions, I asked him if he would ever consider coming to work for our credit union.  Finally, we had an opening for a Branch Manager position and I made a hard pitch to him to join our family.  He agreed and started working at our credit union on September 1, 2005 as a Branch Manager for the Hapeville location. From there the history began.</p>
	<p>From the beginning, he was so great with our members.  Many members that came in for help already knew him because he sold them a car at Enterprise Car Sales.  He was a natural.  He knew how to speak to our members and made them feel comfortable and earned their trust.  Before long, members were calling Carl for advice on all financial matters and he was building important relationships that would last for many years.  Our credit union was growing and he was a very important contributor to our success.  For those that remember, he started the “It is a great day at Fulton Teachers’ Credit Union, can I help you” when we answered the phones.</p>
	<p>In 2008, our credit union had a job opening for Director of Marketing and Business Development.  Carl expressed interest to me in the position.  I knew Carl would be perfect for the position but he was doing a great job as Branch Manager and I wanted him near me for his council and direction.  He was very persuasive and convinced me to promote him to the position.  He transferred to the Roswell branch to start as the Director of Marketing and Business Development.  He worked with his team to start visiting our membership.  He went to the schools, the Administration Center, the Transportation Department, the Maintenance Department, the Nutrition Department, and any other place that would let him speak.  Again, he was a natural, a self-starter.  Before long, everyone knew and loved Carl Blouin.  He was becoming a celebrity in Fulton County.  I would attend events with Carl and it seemed that he knew everyone and they would all come up and want to hug him.  He was larger than life and the members truly loved Carl.</p>
	<p>Our credit union began a strong relationship with the Transportation Department by having a “Member Appreciation Cookout” for the bus drivers and by sponsoring the Fulton’s Finest Program.  At that point, our annual cookouts were born and we eventually added the Administration Center. Carl was from New Orleans, Louisiana and was an amazing chef.  He was in charge of the events and on many occasions, we would feed more than 400 people at a time.  He would cook the hamburgers and made all the events a party.  Everyone knows about the famous “Jambalaya” that he would serve from his secret recipe.  The members would come over to speak to him and want to joke around about the Saints or other things.  It was amazing that he could create a special relationship with so many people that truly loved Carl.  He cooked out for the annual Bus Rodeo event and would speak to the bus drivers about the services of our credit union.  Carl had a gift of speaking to people.  He would tell relatable stories from his heart which made people want to join our credit union family. He was an amazing speaker.</p>
	<p>Around 2010, Carl began serving on the Board of Directors of the Fulton Education Foundation.  I asked Carl to serve and represent our credit union.  He said it would be an honor to serve. He developed very strong relationships with the other board members and the FCBOE staff.  It wasn’t long before Darlene Trigg was calling me asking if Carl could serve as the Chairman of the Board.  Carl was doing what he does everywhere he goes; he was a very successful person.  Carl served as the Chairman and led the Legacy of Excellence event where he spoke in front of over 1,000 people.  I was so proud of him for his accomplishments and being a steward of our credit union.  Again, he was larger than life and our members truly loved Carl.</p>
	<p>On one occasion, Fulton County Board of Education was having an event and Carl was asked to be part of the program.  He was the entertainment and played the saxophone solo in front of a large room full of people.  He had no fear and nothing was unattainable to him.  Again, he was a high achiever and made the credit union look great.</p>
	<p>At one point, Carl was summoned for jury duty.  We were very busy at the time and I needed him at the credit union.  He had no choice and had to report.  I sat him down and told him that he needed to keep his mouth closed so he could get back to work as soon as possible.  He did not listen to me. He earned the trust of his fellow peers in a short time and became the “Jury Foreman”.  Again, another example of him leading and his peers believing in him.</p>
	<p>In 2012, Carl was promoted to Chief Lending Officer.  He had proven himself over and over to us in every position he held and was needed to lead our lending team.  He had earned our entire board support and this would be a new chapter for him.  Very quickly, he took over the lending function and led our credit union to many successful years in lending.  He built a great lending team though the years by coaching our Loan Officers and instilling our lending philosophy in each of them.  He was not the type to sit in his office away from the membership, he preferred to be in the branch helping our members with their needs.  He led by example and never shied away from an uncomfortable situation with a member.  Our employees saw his love for the members and it was contagious.  They saw his belief in our mission of helping people and they strived to provide the same great customer service.  That is what made him such a good leader and ambassador of the credit union.</p>
	<p>Ten months after Carl was hired, our credit union hired Jim Helms who is our Chief Financial Officer and EVP.  The chemistry between the three of us was very strong.  We worked together and formed a very close bond through the years.  It is very rare to have a financial institution where management sticks together and isn’t pulled apart by outside forces.  Carl’s job was not limited to the title of Chief Lending Officer, he had many other responsibilities in running the credit union.  I depended on Carl for his advice on so many different topics and I was always confident in his assessments of the different situations.  He always was with me when we would interview potential employees for job positions.  I valued his opinion and knew he had the skill and ability to identify people who could help fulfill our mission.  He was very important to the strategic planning of the credit union.  He was an integral part of management and contributed to the following:</p>
	<p>Members – Carl was amazing with our members and had built heart felt relationships with so many.  He was not loved a little by some, he was loved so much by so many.  He truly made a huge difference and impacted so many lives in his twelve years at the credit union.  He believed that our members were an extension of our “Family” and we should treat all of them that way.</p>
	<p>Employees – What can I say, Carl was Carl.  Everyone loved him.  He had a unique relationship with each employee.  Every morning, he would go around the credit union and spend time saying wishful thoughts for the day to all of the employees.  If someone was having a bad day, he would say “It’s OK, you are in the 10% now but you will be back in the 90% soon”.  He meant that only 10% of your job might be bad but that 90% is great.  He would lead by example and everyone knew they could speak to him at any moment on any subject to get his direction.  Carl represented what “Family” meant to our credit union with his love to his fellow employees.</p>
	<p>Board of Directors – Carl had a deep relationship with our board members.  They had total confidence in him and knew that our members would be well served under his leadership in lending.  He was known for his famous “Bread Pudding” that he would make occasionally for our meetings. Carl helped management lead the board in our board meetings, strategic planning sessions, credit committee meetings, and so much more.  They saw the love and passion in his heart that he had for our members and his desire to contribute in our mission of making our members feel like being part of our “Family”.</p>
	<p>Finally, I have known Carl for 15 years and it has been a true pleasure serving our members with him. He served as a true steward of Family First Credit Union and wore our mission on his sleeve.  He loved the credit union and our members with all of his heart.  He has meant so much to me and I will forever hold our memories dear to my heart.  First, he was my Chief Lending Officer, second, my go to person for advice or sound reasoning to the different situations, third, a true friend that would do anything for me, and fourth, a brother in life that I loved dearly.  Someone like Carl is not replaceable.  He was larger than life and was always the life of the party.  I will miss his smile, companionship, and amazing attitude on life.</p>
	<p>Carl is married to Monica Blouin and has two beautiful children, Myles Blouin and Ireland Blouin.  Our credit union family will keep the Blouins in our prayers and will miss Carl dearly.</p>
	<p>Please take a few minutes to remember Carl Blouin for who he was with his larger than life personality and the good times that we all had with him.</p>

