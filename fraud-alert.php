<h2>Fraud Alert Center</h2>
	<p><img src="images/handprint.png" alt="Fraud Alert" align="right">Protecting yourself from Fraud & Identity Theft is incredibly important and Family First Credit Union wants to help keep you safe. </p>
	
	<p><strong><u>IDENTITY THEFT:</u></strong><br>
Identity Theft occurs when someone uses your name, social  security number, credit card number, or other identifying data to commit fraud  or other crimes. &nbsp;In this electronic age, it has become an all-too-common  danger. &nbsp;Fortunately, there are many preventative measures you can take to  substantially reduce the chance of identity theft occurring and steps you can take  to minimize damage if you do become a victim. &nbsp;Click on any link below to  gain valuable information on how to keep yourself protected. &nbsp;And speak with an  GreenPath counselor if you feel you’ve been a victim of fraud or identity theft by  visiting <a href="#" data-remodal-target="greenPath">https://www.greenpath.com</a></p>
	<ul><li><i class="fa fa-check-circle-o"></i> <a href="https://www.greenpath.com/university/identity-theft/all" title="Important Information about Identity Theft" target="_blank" onclick="drawAlert()">Important Information about Identity Theft</a></li>
		<li><i class="fa fa-check-circle-o"></i> <a href="https://www.accelservices.org/credit-union-members/resource-center/articles/identity-crisis-what-do-if-your-identity-stolen" title="What to do if your identity is stolen" target="_blank" onclick="drawAlert()">What to do if your identity is stolen</a></li>
		<li><i class="fa fa-check-circle-o"></i> <a href="pdf/IdentityTheftWorkbook.pdf" title="Identity Theft Workbook" target="_blank">Identity Theft Workbook</a></li>		
	</ul>
	<p>
      <strong><u>VARIOUS TYPES OF FRAUD</u></strong>:<br>
      Fraud can take on many faces and can target anyone.&nbsp;  Learn more about each type below.
	</p>
	<div class="accordion_holder" data-anim-type="fade-in-up" data-anim-delay="100">
          <div class="accordion_example1 smk_accordion acc_with_icon"> 
            
            <!-- Section 1 -->
            <div class="accordion_in three">
              <div class="acc_head"><div class="acc_icon_expand"></div><div class="acc_icon_expand"></div>Phishing</div>
              <div class="acc_content"> <p>Phishing scams generally come in the form of an email supposedly from a company (such as a financial institution, bill pay provider or shopping service) with which you may or may not do business.  The email message is urgent and requests that you update your account information because of system requirements, to avoid your account being closed, or because of possible fraud on your account.  These messages appear authentic and usually include the real company's logo, slogan, and what appears to be its website, where the email message asks that you enter your information.  These sites may even be secure, giving you a false sense of confidence.</p>
				  <p>So how do you know whether the email is real or phony?  Simple - no reputable company would ask customers to verify account information online.  Also, these messages are sent in bulk as spam and use the names of large, well-known companies, taking a chance that you are a customer of that company; if you are not, that should be your first clue to delete the message.  Another clue is that phishing emails often, though not always, contain poor spelling and grammar.</p>
				  <p><strong>Tips to protect yourself from Phishing</strong></p>
				  <ul>
					  <li><i class="fa fa-check-circle-o"></i> Never click on a link in an email if there is a possibility it is fraudulent.  Even without entering personal information on the site, clicking on the link - even to unsubscribe - could install a virus or spyware on your computer and retrieve personal information without your knowledge.</li>
					  <li><i class="fa fa-check-circle-o"></i> Never provide your personal or financial information via email or an unsecured Web page, even if it is to a person or company you know.</li>
					  <li><i class="fa fa-check-circle-o"></i> Keep your computer updated with the most current virus protection software and pop-up blockers.</li>
					  <li><i class="fa fa-check-circle-o"></i> If you go online to shop or conduct financial transactions, be sure to access the site by typing in the address manually.</li>
					  <li><i class="fa fa-check-circle-o"></i> Before you begin a financial or personal transaction online, be sure the site's URL begins with "https" and look for the locked icon in the lower right-hand corner of the browser window.  (It's not enough for a lock to be displayed on the Web page.)</li>
					  <li><i class="fa fa-check-circle-o"></i> Don't be intimidated by urgent email messages warning of the consequences of not following its instructions.</li>
					  <li><i class="fa fa-check-circle-o"></i> If you feel that the email is legitimate, contact the sender using a Web address or phone number that you know is valid, perhaps from a statement you've received.</li>
					  <li><i class="fa fa-check-circle-o"></i> If you are a victim of a phishing scam, contact the company whose name was on the message using a known phone number, place alerts on your credit files, and monitor your account statements closely.</li>
					  <li><i class="fa fa-check-circle-o"></i> Report suspicious emails and  phone calls to the Federal Trade Commission at <a href="https://www.ftc.gov/idtheft/" title="https://www.ftc.gov/idtheft/" target="_blank" onclick="drawAlert()">https://www.ftc.gov/idtheft/</a> or call 1-877-ID-THEFT.</li>
				  </ul>
				</div>
            </div>
			   <!-- Section 2 -->
            <div class="accordion_in three">
              <div class="acc_head"><div class="acc_icon_expand"></div><div class="acc_icon_expand"></div>Vishing</div>
              <div class="acc_content">
					<p>Vishing mimics phishing by trying to trap you into divulging your account numbers.  But instead of being phished in an email message, you may receive a telephone call from an automated random dialer, and the voice on the other end of the line may tell you your credit card has been used illegally.  You're then asked to dial a fake 1-800 number with another voice that asks you to confirm your account details and credit card number.</p>
					
				  <p>All this is possible because of Voice over Internet Protocol (VoIP), the new technology that makes inexpensive and anonymous Internet calling possible.  While there are legitimate reasons to disguise a phone number on caller ID, consumers now have a more difficult time determining fraud from valid contact.  The most important thing to remember, however, is to never give any personal or financial information over the phone if you didn't initiate contact.
					</p>
				  <p><strong>Tips to protect yourself from Vishing</strong></p>
				  <ul>
					  <li><i class="fa fa-check-circle-o"></i> If you get a phone call and someone asks you to give or confirm credit card or personal information, hang up.  Then call your credit union or the financial institution that issued the card by using the phone number on the back of the card or on your statement and report the attempt.</li>
					  <li><i class="fa fa-check-circle-o"></i> If you get a call from someone who claims to be from a financial institution you do business with and who knows your credit card account number but wants the three-digit code on the back of the card, immediately hang up.</li>
					  <li><i class="fa fa-check-circle-o"></i> If you get an email message asking you to call a toll-free number to verify account information, delete the email.  Never provide personal information or account information based on an email request.</li>
					  <li><i class="fa fa-check-circle-o"></i> Don't be fooled by the fact that the caller's phone number appears to be a regional telephone number; it could have been spoofed using Voice over Internet Protocol.</li>
					  <li><i class="fa fa-check-circle-o"></i> Be suspicious of any phone or email contact that doesn't use your first name or surname.</li>
					  <li><i class="fa fa-check-circle-o"></i> Never dial a call return number - or reply to an email - regarding any financial matter.</li>
				  </ul>
				</div>
            </div>
			  <!-- Section 3 -->
            <div class="accordion_in three">
              <div class="acc_head"><div class="acc_icon_expand"></div><div class="acc_icon_expand"></div>ATM Skimming</div>
              <div class="acc_content">
					<p>A method that generally involves criminals tampering with the card reader.  Your card may seem jammed when you insert it into the ATM, and someone may walk up and suggest that you re-enter your personal identification number (PIN).  When that doesn't work, you may give up and leave, but that person - the thief - can retrieve your card along with the device he or she placed on the machine.  The thief can use your card to withdraw money (since he/she watched you enter your PIN).  Or, the thief may watch you enter your PIN via a camera a distance from, or even attached to, the ATM.
					</p>
				  <p>Sometimes, your transaction may proceed uninterrupted, but a skimmer attached to the card reader captures the information on your card, or a fake PIN pad or covering of the existing pad allows the thief to steal your PIN.  While you'll get your card back, the thief can use the information from your card to replicate the card and withdraw cash.  If it's a debit card, he or she can even make signature-based purchases at most retail locations with the counterfeit card.  In another situation, the thief may be able to intercept your cash at the end of the transaction by adding a false front on the cash dispenser, which traps the bills inside.  It may seem that the machine is malfunctioning, but once you leave, the thief will claim your cash.</p>
				  <p><strong>Tips to protect yourself from ATM Skimming</strong></p>
				  <p>The best way you can protect yourself is to be alert. If an ATM looks different or as if it has been tampered with, do not use it.  If you see anyone standing near the ATM or watching the machine from a distance, leave and find another machine.</p>
				</div>
            </div>
			   <!-- Section 4 -->
            <div class="accordion_in three">
              <div class="acc_head"><div class="acc_icon_expand"></div><div class="acc_icon_expand"></div>Credit Card & Debit Card Fraud</div>
              <div class="acc_content">
					<p>While theft of a purse or wallet is a big red flag for credit card fraud, it is important to note that thieves do not need possession of your card in order to use it; they can simply use your card number to make purchases over the phone or online.  They can get this information in a variety of ways: looking through trash to find receipts or statements with your credit card number on it (also known as dumpster diving); luring you to give them your credit card information through the mail, over the phone or online; or capturing the information after you've used the card, either at the store or through the credit card processor.
					</p>
				  <p><strong>Tips to protect yourself from credit card (and debit card) fraud</strong></p>
				  <ul>
					  <li><i class="fa fa-check-circle-o"></i> Although you may not be able to keep hackers or dishonest employees out of your credit card processor's office, you can take simple defensive measures to keep thieves from stealing your credit card information from the garbage.  (Note: Because debit cards can be used at many of the same places you can use a credit card, these tips apply to both.)</li>
					  <li><i class="fa fa-check-circle-o"></i> Make sure your credit card company has your current phone number so it can contact you if it sees suspicious charges on your card.</li>
					  <li><i class="fa fa-check-circle-o"></i> Don't use a debit card for online purchases.  Unlike credit cards, most debit cards don't have a limited fraud liability.</li>
					  <li><i class="fa fa-check-circle-o"></i> Don't authorize payments or give out personal information over the phone unless you call a specific creditor and know its reputation.</li>
					  <li><i class="fa fa-check-circle-o"></i> Review your credit card and checking account statements regularly.  Many thieves will "test out" a card by charging small amounts, such as at a gas station.</li>
					  <li><i class="fa fa-check-circle-o"></i> Notify your card issuer if a monthly statement fails to arrive in the mail.  Criminals may have changed the address on your account to hide fraudulent activity.</li>
					  <li><i class="fa fa-check-circle-o"></i> Check your account statements for errors as soon as they arrive.  If there is an error, contact the financial institution as soon as possible.</li>
					  <li><i class="fa fa-check-circle-o"></i> Shred pre-approved credit card offers, statements, and bills that contain account numbers or other personal information before throwing them away.</li>
					  <li><i class="fa fa-check-circle-o"></i> Keep a record of your account numbers, card expiration dates, and the card issuers' phone numbers in a safe place - but not in your wallet.</li>
					  <li><i class="fa fa-check-circle-o"></i> Be careful about using your credit card at Internet cafes or anywhere with a wireless connection.</li>
					  <li><i class="fa fa-check-circle-o"></i> Check your credit report  every year for errors and unauthorized accounts.&nbsp; Go to <a href="https://www.annualcreditreport.com/" title="www.annualcreditreport.com" target="_blank" onclick="drawAlert()">www.annualcreditreport.com</a> to request a <em>free</em> credit report from each of the major credit  reporting agencies - Experian, Equifax, and TransUnion. &nbsp;Stagger your requests. &nbsp;For example, get one free report from Experian, in four months get a free  report from TransUnion, and in another four months get a free report from  Equifax.</li>
				  </ul>
