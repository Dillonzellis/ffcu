<h2>Club Accounts</h2>

	<p>Ever wish you had extra cash for holiday shopping or summer vacation?  Well, you can when you save throughout the year with our Christmas Club and Vacation Club accounts!  You can have funds allocated to these accounts through payroll deduction.  These club accounts are another type of savings account but the balance is dispersed directly into your savings account (or via check if preferred) when it gets closer to that occasion.  For instance, start saving now for Christmas and you'll get the balance saved in the club account in October, or start saving for next summer when checks for the Vacation Club get dispersed in May.</p>
	<p>Family First Credit Union offers three different club accounts.  The dividends are posted quarterly on the following accounts:</p>
	<ul>
		<li><i class="fa fa-check-circle-o"></i> <strong>Christmas Club -</strong> This account allows you to save monthly through payroll deduction for those extra expenses incurred during the holidays. &nbsp;You can set a predetermined amount to be deducted from your paycheck each month to go into your Christmas Club Account. &nbsp;Each quarter your deposits accrue interest and in October the total amount deposited will be transferred to  your savings account, unless otherwise requested. &nbsp;This is a convenient and easy way to save for those extra expenses and will eliminate the financial stress around the holidays. See the <a href="share-rates.php" title="rate section">rates section</a> for our current Club Account rates.</li>
		<li><i class="fa fa-check-circle-o"></i> <strong>Vacation Club -</strong> This account allows you to save monthly through payroll deduction for planned vacations throughout the year. &nbsp;You can set a predetermined amount to be deducted from your paycheck each month to go into your Vacation Club Account. &nbsp;Each quarter your deposits accrue interest and in May the total amount deposited will be transferred to  your savings account, unless otherwise requested. &nbsp;So start saving today for those much deserved vacations. &nbsp;See the <a href="share-rates.php" title="rate section">rates section for our current Club Account rates</a>.</li>
		<li><i class="fa fa-check-circle-o"></i> <strong>Ten Month   Club Account                          -</strong> If you get paid only ten months out of a year, this account is for you. &nbsp;This account allows you to save monthly through payroll deduction for the summer months when you do not get a paycheck. &nbsp;Each quarter your deposits accrue interest and in July and August the total amount deposited will be transferred to  your savings account, unless otherwise requested. Start budgeting now so you will have adequate cash flow for twelve months a year, instead of just ten months a year. See the <a href="share-rates.php" title="rate section">rates section</a> for our current Club Account rates.</li>
		
	</ul>
	<p><br>
  If you would like to enroll for a club  account, simply fill out a <a href="pdf/ClubAccount.pdf" title="Club Account Application" target="_blank">Club Account Application</a> and check the club you would  like to join along with the amount you would like to deduct MONTHLY from your paycheck.  &nbsp;If you do not wish to enroll in Payroll Deduction, but would still like to  enroll in a club account, deposits can be made by mailing a personal check to  the credit union or visiting either branch to make a deposit.</p>
	<p>If you have any questions about Club  Accounts please <a href="contact.php" title="contact us">contact us</a> or you can <a href="calculators/savings/sc.html" title="click here" target="_blank">click here for the savings calculator</a>.</p>
	<p>See the <a href="share-rates.php" title="rate section">rates section</a> for our current Club Account rates. </p>



