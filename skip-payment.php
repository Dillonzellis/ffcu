	<p>Please note, if you skip payments on your auto loans and you purchased GAP insurance, this will affect the payout based on the additional interest and principal.  If you skip more than five payments, your GAP insurance is voided.  Loans will not payout according to the original terms of the loan agreement if a payment is skipped.  Real Estate loans are not included (contact a Loan Officer).</p>
	
	
	
