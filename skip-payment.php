	<p>Due to the uncertainty of times with the Coronavirus COVID-19, Family First Credit Union is offering the “Skip-A-Payment Assistance” to members to assist in the loss of household wages, unexpected expenses, and safety preparedness. </p>

	<p>Members can apply to skip up to two consecutive payments on their consumer loans and credit cards to help relieve paying bills during this difficult time.  We will waive all skip payment fees associated with the Coronavirus COVID-19 crisis.  If you have skipped a payment in the last six months due to another reason, you are still eligible for two consecutive payment skips associated with the Coronavirus COVID-19 crisis. </p>

