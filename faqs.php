				<ul>
					<li><i class="fa fa-check-circle-o"></i> Member ownership - Each member of a credit union is a part owner of that credit union and is entitled to one vote at the annual meeting, regardless of how much money he or she has deposited in the credit union.</li>
					<li><i class="fa fa-check-circle-o"></i> Members come first - Because credit unions are not-for-profit cooperatives, they are usually able to offer lower loan rates, higher savings rates and lower fees than for-profit institutions.</li>
					<li><i class="fa fa-check-circle-o"></i> Special services - Credit unions serve their members and can offer special services that for-profit institutions may not always offer.  Examples include signature loans under $1,000 to help members through financial difficulties, savings clubs for kids, and financial counseling.</li>
					<li><i class="fa fa-check-circle-o"></i> Volunteer leadership - Credit unions are led by a board of directors elected from the membership at the annual meeting.  Board members volunteer their time and service and are not compensated.</li>
