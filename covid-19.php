<h2 style="color: #b10021;"><strong>PROTECT OUR COMMUNITY: CORONAVIRUS DISEASE (COVID-19)</strong></h2>

	 <p>For  the latest updates, from <strong>The WHO</strong> on the Coronavirus (COVID-19), and how  to keep you and your family safe, <a href="#" data-remodal-target="who">click  here</a>.</p>
    <p>To stay up-to-date on the latest news in our area, <a href="#" data-remodal-target="dph"> click here</a> to visit the <strong>Georgia Department of Public Health Daily Status Report</strong>.</p>
     <p><strong>Updated April 7, 2021</strong></p>
    <p>We are happy to announce that Family First Credit Union is reopening our branches beginning April 12, 2021.  During these uncertain times, the safety of our staff and members is of the utmost importance to management.  We want to be safe while continuing to provide our members with the best products and services while offering outstanding customer service.  At this time, we will be following these guidelines:</p>

    
    <ul class="bullets">
		<li>We are asking that members wear a mask when entering the building.  If a member does not have a mask, we will provide you with one.
	  </li>
		<li>To help stay safe, the Credit Union will limit the number of members in the lobby to seven members.  
	</li>
		<li>The Credit Union will practice social distancing in all areas of the branch.  Members are limited to their transaction area only.
	</li>
		<li>Each member facing employee has a plexiglass sneeze protector barrier.
	</li>
		<li>The floors have been marked so that members are 6 feet apart.
</li>
		<li>Workstations and the Teller area will be cleaned and disinfected several times a day.
</li>
		<li>At this time, the restrooms are closed to the members.
</li>
		<li>A hostess will be working the front door.  The hostess will control the line and visits to the Tellers, Member Service Representatives, and Loan Officers
</li>
		<li>Members are still encouraged to use the drive-thru if possible.</li>

    </ul><br>
	  
	  <p>Hopefully, more and more people will get vaccinated, and the Covid-19 numbers will reach a point low enough to where we will be able to go back to business as usual without any limitations on our branch services.  </p>
	  
	  <p>We encourage members to continue to use our drive-thru for assistance.  The Credit Union will continue to process teller transactions and offer additional services at the drive-thru which can handle almost all member transactions.  Examples of those transactions are listed below:</p>
	  
	  <ul class="bullets">
		  <li>Deposits and Withdrawals</li>
		  <li>Debit Card issues</li>
		  <li>Cashier’s Checks or Official Checks</li>
		  <li>Loan Payments</li>
		  <li>Account transaction issues</li>
		  <li>Check requests (limited to one per car)</li>
		  <li>Notary Services</li>
		  <li>Account openings and changes to accounts</li>
	  </ul><br>
	  
	  <p>Requests for account maintenance, histories, transaction details, balances, etc., will continue to be handled over the phone or by our online banking platform.  Most loans are closed by IMM eSign from the comfort of your home.  You can apply for loans online at <a href="https://www.ffcuga.org/">www.ffcuga.org</a> or by calling (404)768-4980.  Mobile and Online Banking allows you to transfer funds, pay bills, get account history, and more, all from the safety of your home.</p>
	  
	  <p>Family First is offering the following solutions to help provide relief to members that have suffered a loss of household wages, unexpected expenses, and safety preparedness due to the Coronavirus COVID-19:</p>
	  
	  <ul class="bullets">
		  
		  <li><a href="https://www.ffcuga.org/forms_app/skip-payment.php"><strong>Skip-A-Payment Assistance</strong></a> – Members can apply to skip up to two consecutive payments on their consumer loans to help relieve paying bills during this difficult time. We will waive all skip payment fees due to the Coronavirus COVID-19 crisis.</li>
		  <li><strong>Loan Modification</strong> – If the “Skip-A-Payment Assistance Program” does not help your current situation due to the Coronavirus COVID-19 crisis, management will consider a “Loan Modification” which may include payment deferrals or extensions of repayment terms. This will be on a case-by-case basis.</li>
	  </ul><br>
	  
	  <p>We have many <strong>remote solutions</strong> for you to protect you during these difficult times.  Please click the links below to take advantage of these options…</p>
	  
	  <ul class="bullets">
		<li><strong>Call Center</strong> – Contact our Call Center Monday – Friday from 9am – 5pm and Saturday from 9am to 12pm at 404-768-4980 (Hapeville) or 770-667-8114 (Roswell).  <a href="https://www.ffcuga.org/siteinfo/contact.php" target="_blank">Contact Us</a></li>
		<li><strong>Audio Response System (24 Hour Express Line Teller)</strong> – Did you know that we have a 24-hour telephone response system, which is a major convenience during these times?  You can check your account balances and transactions at the touch of your phone keypad.  <a href="https://www.ffcuga.org/services/phone.php" target="_blank">Express Line Teller</a></li>
		<li><strong>Online Banking </strong>– During these trying times, Family First wants to make your convenience our priority.  You can remotely access account information 24 hours a day, just like with our Express Line Teller, but on your smart device. <a href="https://www.ffcuga.org/services/onlinebanking.php" target="_blank">Click here for online banking</a>.</li>
		<li><strong>Bill Pay</strong> – Save your money that’s normally spent on checks and postage, Family First has FREE bill pay options!  <a href="https://www.ffcuga.org/services/onlinebanking.php" target="_blank">Free Bill Pay</a></li>
		<li><strong>Mobile Banking</strong> – You can view your account balances and perform account transfers directly from your mobile device. <a href="https://www.ffcuga.org/services/onlinebanking.php" target="_blank">Mobile Banking</a></li>
		<li><strong>Mobile Check Deposit</strong> – Do you have a check that needs to be deposited?  No worries, do it remotely from the safety of your own home with our convenient online mobile check deposit.  <a href="https://www.ffcuga.org/services/onlinebanking.php" target="_blank">Mobile Check Deposit</a></li>
		<li><strong>Allpoint Network</strong> – Did you know that you have over 55,000 FREE ATM machines within a 5-mile radius of your location?  This also includes all our branch and off-site locations. <a href="https://www.ffcuga.org/services/atm.php" target="_blank">ATM Services</a></li>
		<li><strong>Loans</strong> – Family First Credit Union is aware of the uncertain times that lie ahead.  We are here for you!  Visa Cards, Signature Loans, Auto Loans, HELOC’s…check us out! <a href="https://www.ffcuga.org/loans/index.php" target="_blank">Loans</a></li>
	  </ul><br>
	  
	  <p><strong>Learn How to Spot Scammers</strong></p>
	  
	  <p>Unfortunately, scammers really attempt to take the utmost advantage in uncharted times. People will try to play on your fears of COVID-19 by offering fraudulent products, creating fraudulent charities or coaxing you to click fraudulent links and gaining access to your personal information.  Help protect yourself by remaining aware and following these tips:</p>
	  
	  <ul class="bullets">
		<li>Be cautious of anyone attempting to prey on your emotions by using threatening messages.</li>
		<li>Take special note of emails containing links and never provide confidential information unless you initiated the conversation.</li>
		<li>Be mindful of attempts to impersonate your employer.</li>
		<li>Watch for bad grammar and misspellings in unsolicited emails and text messages.</li>
		<li>When in doubt, call back.  If someone calls you claiming to be from a financial institution or any person requesting your personal information, hang up and call the correct number for the institution to verify its legitimacy.</li>
	  </ul><br>
	  
	  <p>If you think you may have fallen victim to one of these scams, be sure to call Family First Credit Union as soon as possible at 404-768-4980 or 770-667-8114.  There is always someone standing by to help you.</p>
	  
