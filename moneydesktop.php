<h2>MoneyDesktop</h2>
	
	<p>MoneyDesktop is a PFM – a personal financial management tool – that helps you budget and manage your finances. It allows you to import all of your accounts, across multiple institutions, so you can see your balances and transactions all in one place.</p>
	<ul>
		<li><i class="fa fa-check-circle-o"></i> Import data from your primary institution and other institutions</li>
		<li><i class="fa fa-check-circle-o"></i> Categorize loan payments and transfers</li>
		<li><i class="fa fa-check-circle-o"></i> Save transaction and budget history</li>
		<li><i class="fa fa-check-circle-o"></i> Track Interest Paid</li>
		<li><i class="fa fa-check-circle-o"></i> Create Budgets</li>
		<li><i class="fa fa-check-circle-o"></i> Set Alerts</li>
		<li><i class="fa fa-check-circle-o"></i> and more!</li>
